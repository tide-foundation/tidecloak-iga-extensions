    private static void replayCreateUser(KeycloakSession session, RealmModel realm,
                                          org.tidecloak.iga.entities.IgaChangeRequestEntity cr,
                                          List<Map<String, Object>> rows, String sig, EntityManager em) {
        for (Map<String, Object> row : rows) {
            String userId = str(row, "ID");
            rebuildCreateUserFromRow(session, realm, row);
            // D3 — auto-assign the realm default-role + default groups at COMMIT-replay, so
            // the token the new user mints carries the default-role-derived claims. The capture
            // path (IgaUserProvider.addUser, addDefaultRoles=false) deliberately leaves the
            // PENDING user role-less; the default-roles land HERE at commit. This runs under
            // IGA_REPLAY_ACTIVE=true (set by replay()), so user.grantRole / user.joinGroup do
            // NOT spawn a nested GRANT_ROLES/JOIN_GROUPS CR. Mirrors stock KC
            // JpaUserProvider.addUser (grant getDefaultRole + join getDefaultGroupsStream).
            // The granted default edge is NOT per-user signed (D1+D1b drop it) — the
            // realm_default_roles_set authority + universal-inherit covers it.
            org.keycloak.models.UserModel created = session.users().getUserById(realm, userId);
            if (created != null) {
                org.keycloak.models.RoleModel defaultRole = realm.getDefaultRole();
                if (defaultRole != null) {
                    created.grantRole(defaultRole);
                }
                realm.getDefaultGroupsStream().forEach(created::joinGroup);
            }
            em.createQuery("UPDATE UserEntity e SET e.attestation = :sig WHERE e.id = :id")
                    .setParameter("sig", sig)
                    .setParameter("id", userId)
                    .executeUpdate();
        }
        // FINALIZE the persisted-pending user (enroll-before-commit / Tideless
        // quarantine): clear any IGA_UNSIGNED_ENTITY sidecar row this CREATE_USER
        // CR registered when the pending user was PERSISTED quarantined (the
        // Tideless path in IgaUserAdapter.getId). Keyed on the CR id, so it is a
        // no-op for the Tide path (no sidecar was written there — replayOrFailClosed
        // is the guard) and for any historical rollback-style CREATE_USER CR. The
        // user_identity producer column is stamped over the LIVE (post-enrollment)
        // attributes by the TideAttestor commit path (CREATE_USER → stampUserIdentity
        // / the multiAdmin carrier), un-quarantining the now-finalized user.
        if (cr != null) {
            org.tidecloak.iga.services.IgaUnsignedEntityService.clearByAdoptCr(em, cr.getId());
        }
    }

    private static void replayCreateRole(KeycloakSession session, RealmModel realm,
                                          List<Map<String, Object>> rows, String sig, EntityManager em,
                                          boolean setSigned) {
        for (Map<String, Object> row : rows) {
            String id = str(row, "ID");

            RoleModel role = rebuildCreateRoleFromRow(session, realm, row);
            if (role == null) continue;

            em.createQuery("UPDATE RoleEntity e SET e.attestation = :sig WHERE e.id = :id")
                    .setParameter("sig", sig)
                    .setParameter("id", id)
                    .executeUpdate();

            // Stamp the composite-role edges created above for THIS parent role.
            // RepresentationToModel applied each composite link via
            // role.addCompositeRole(...) (a CompositeRoleEntity row keyed
            // (parent,child)); the root RoleEntity stamp above leaves those
            // edge rows unattested.
            if (sig != null && !sig.isEmpty()) {
                if (setSigned) {
                    // SET-SIGNING (tide): the composite children form a per-
                    // (composite_role, parent) SET. Sign that set independently
                    // (owner = this parent role) so it is re-derivable exactly
                    // like a dedicated ADD_COMPOSITE — NOT the node's per-entity
                    // sig. No-op when the role has no composites.
                    signNestedChildSet(session, em, "ADD_COMPOSITE", id, "parentRole.id");
                } else {
                    // simple attestor (UNCHANGED): stamp the node sig per-edge,
                    // covering every child of this parent (e.parentRole.id).
                    em.createQuery("UPDATE CompositeRoleEntity e SET e.attestation = :sig WHERE e.parentRole.id = :id")
                            .setParameter("sig", sig)
                            .setParameter("id", id)
                            .executeUpdate();
                }
            }
        }
    }

