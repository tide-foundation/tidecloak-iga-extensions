    /**
     * Replay CREATE_CLIENT_SCOPE: realm.addClientScope(id, name) plus optional
     * protocol/description, then stamp the attestation onto ClientScopeEntity
     * via JPQL. Requires the tidecloak-override commit that adds an
     * {@code attestation} field on {@code ClientScopeEntity}; without it this
     * UPDATE will fail at runtime.
     */
    private static void replayCreateClientScope(KeycloakSession session, RealmModel realm,
                                                  List<Map<String, Object>> rows, String sig, EntityManager em,
                                                  boolean setSigned) {
        for (Map<String, Object> row : rows) {
            String id = str(row, "ID");
            String name = str(row, "NAME");
            if (id == null || name == null) continue;

            if (rebuildCreateClientScopeFromRow(realm, row) == null) continue;

            if (sig != null && !sig.isEmpty()) {
                em.createQuery("UPDATE ClientScopeEntity e SET e.attestation = :sig WHERE e.id = :id")
                        .setParameter("sig", sig)
                        .setParameter("id", id)
                        .executeUpdate();

                // Stamp the scope's nested protocol mappers. RepresentationToModel
                // .createClientScope persists each ProtocolMapperEntity (keyed by
                // its own id, FK CLIENT_SCOPE_ID = this scope's UUID) via
                // scope.addProtocolMapper(...); the root ClientScopeEntity stamp
                // above leaves those mapper rows unattested.
                if (setSigned) {
                    // SET-SIGNING (tide): the scope's protocol mappers form a
                    // per-(protocol_mapper, scope) SET. Sign that set
                    // independently (owner = this scope) so it is re-derivable
                    // like a dedicated ADD_PROTOCOL_MAPPER — NOT the node sig.
                    // protocol_mapper owner field for a scope-owned mapper is
                    // clientScope.id. No-op when the scope has no mappers.
                    signNestedChildSet(session, em, "ADD_PROTOCOL_MAPPER", id,
                            org.tidecloak.iga.attestors.TideSetResolver.PROTOCOL_MAPPER_SCOPE_OWNER_FIELD);
                } else {
                    // simple attestor (UNCHANGED): node sig per-mapper, owner-
                    // keyed (e.clientScope.id), covering every mapper of this scope.
                    em.createQuery("UPDATE ProtocolMapperEntity e SET e.attestation = :sig WHERE e.clientScope.id = :scopeId")
                            .setParameter("sig", sig)
                            .setParameter("scopeId", id)
                            .executeUpdate();
                }
            }
        }
    }

    /**
     * Replay DELETE_CLIENT_SCOPE: delete the {@code tide-claims} (or any) client
     * scope by id via {@code session.clientScopes().removeClientScope(realm, id)}.
     *
     * <p>This is the teardown counterpart of {@link #replayCreateClientScope}.
     * A SINGLE scope deletion is self-contained: Keycloak's
     * {@code JpaRealmProvider.removeClientScope(realm, id)} (the active
     * {@code ClientScopeProvider} delegate) does, in one call —
     * <ul>
     *   <li>{@code realm.removeDefaultClientScope(scope)} — drops the
     *       {@code DEFAULT_CLIENT_SCOPE} realm-default row;</li>
     *   <li>{@code deleteClientScopeClientMappingByClientScope} — drops every
     *       {@code CLIENT_SCOPE_CLIENT} per-client attachment;</li>
     *   <li>{@code deleteClientScopeRoleMappingByClientScope} — drops the
     *       scope's role-mapping allow-list;</li>
     *   <li>{@code em.remove(clientScopeEntity)} — drops the scope, whose nested
     *       {@code PROTOCOL_MAPPER} rows (incl. the inline {@code t.uho} mapper)
     *       cascade away via the {@code ClientScopeEntity.protocolMappers}
     *       {@code orphanRemoval}/cascade-ALL FK.</li>
     * </ul>
     * So one removal CR tears the entire provisioning chain back down — no
     * reverse-ordered detach / remove-default CRs are needed. (Confirmed against
     * Keycloak 26.5.5 {@code JpaRealmProvider.removeClientScope}.)
     *
     * <p>Runs under {@code IGA_REPLAY_ACTIVE} (set by {@link #replay}), so the
     * delete passes straight through the IGA wrappers without being re-captured.
     * No attestation stamp — the rows are deleted (mirrors the
     * {@code DELETE_ORGANIZATION} / {@code REMOVE_SCOPE} revoke replays). The
     * {@code ClientScopeRemovedEvent} published by {@code removeClientScope}
     * drives cache invalidation, so no explicit eviction is needed here.
     */
    private static void replayDeleteClientScope(KeycloakSession session, RealmModel realm,
                                                List<Map<String, Object>> rows) {
        for (Map<String, Object> row : rows) {
            String id = str(row, "ID");
            if (id == null) id = str(row, "SCOPE_ID");
            if (id == null) continue;
            ClientScopeModel scope = session.clientScopes().getClientScopeById(realm, id);
            if (scope == null) {
                log.warnf("DELETE_CLIENT_SCOPE replay: scope %s no longer exists in realm %s; skipping",
                        id, realm.getId());
                continue;
            }
            boolean removed = session.clientScopes().removeClientScope(realm, id);
            log.infof("DELETE_CLIENT_SCOPE replay: scope %s in realm %s removed=%s",
                    id, realm.getId(), removed);
        }
    }

    private static void replayUpdateProtocolMapper(KeycloakSession session, RealmModel realm,
                                                    List<Map<String, Object>> rows, String sig, EntityManager em) {
        for (Map<String, Object> row : rows) {
            String mapperId = str(row, "ID");
            if (mapperId == null) continue;
            String clientUuid = str(row, "CLIENT_UUID");
            String clientId = str(row, "CLIENT_ID");
            String scopeId = str(row, "CLIENT_SCOPE_ID");
            org.keycloak.models.ProtocolMapperModel mapper = new org.keycloak.models.ProtocolMapperModel();
            mapper.setId(mapperId);
            mapper.setName(str(row, "NAME"));
            mapper.setProtocol(str(row, "PROTOCOL"));
            mapper.setProtocolMapper(str(row, "PROTOCOL_MAPPER_NAME"));
            Object cfg = row.get("config");
            if (cfg instanceof Map<?, ?> cfgMap) {
                java.util.Map<String, String> config = new java.util.LinkedHashMap<>();
                for (Map.Entry<?, ?> e : cfgMap.entrySet()) {
                    if (e.getKey() != null) {
                        config.put(e.getKey().toString(), e.getValue() == null ? null : e.getValue().toString());
                    }
                }
                mapper.setConfig(config);
            } else {
                mapper.setConfig(new java.util.LinkedHashMap<>());
            }
            if (clientUuid != null || clientId != null) {
                ClientModel client = resolveClient(session, realm, row);
                if (client != null) client.updateProtocolMapper(mapper);
            } else if (scopeId != null) {
                ClientScopeModel scope = session.clientScopes().getClientScopeById(realm, scopeId);
                if (scope != null) scope.updateProtocolMapper(mapper);
            }
            if (sig != null && !sig.isEmpty()) {
                em.createQuery("UPDATE ProtocolMapperEntity e SET e.attestation = :sig WHERE e.id = :id")
                        .setParameter("sig", sig)
                        .setParameter("id", mapperId)
                        .executeUpdate();
            }
        }
    }

    private static void replayRemoveProtocolMapper(KeycloakSession session, RealmModel realm,
                                                    List<Map<String, Object>> rows) {
        for (Map<String, Object> row : rows) {
            String mapperId = str(row, "ID");
            if (mapperId == null) continue;
            String clientUuid = str(row, "CLIENT_UUID");
            String clientId = str(row, "CLIENT_ID");
            String scopeId = str(row, "CLIENT_SCOPE_ID");
            org.keycloak.models.ProtocolMapperModel mapper = new org.keycloak.models.ProtocolMapperModel();
            mapper.setId(mapperId);
            if (clientUuid != null || clientId != null) {
                ClientModel client = resolveClient(session, realm, row);
                if (client != null) client.removeProtocolMapper(mapper);
            } else if (scopeId != null) {
                ClientScopeModel scope = session.clientScopes().getClientScopeById(realm, scopeId);
                if (scope != null) scope.removeProtocolMapper(mapper);
            }
            // No attestation write — the row is being deleted.
        }
    }

    /**
     * Run a JPQL UPDATE that stamps the final attestation on every row of an
     * attribute entity matching (parent_id, name). Multi-value attributes share
     * the (parent_id, name) tuple — every row gets the same attestation.
     */
    private static void stampSigJpql(EntityManager em, String jpql, String sig, String parentId, String name) {
        if (sig == null || sig.isEmpty()) return;
        em.createQuery(jpql)
                .setParameter("sig", sig)
                .setParameter("pid", parentId)
                .setParameter("name", name)
                .executeUpdate();
    }

    // -------------------------------------------------------------------------
    // Governed IGA disable (DISABLE_IGA)
    //
    // The ON→OFF teardown runs HERE, only after the DISABLE_IGA change request commits
    // (admin-approved at the realm-default quorum). It runs IN ORDER inside the
    // replay transaction (IGA_REPLAY_ACTIVE is already true, set by replay()):
    //
    //   1. isIGAEnabled=false — the actual flag write, suppressed (IGA_REPLAY_ACTIVE
    //      is active, so IgaRealmAdapter.setAttribute passes straight through to
    //      super and does NOT re-capture this as a SET_REALM_ATTRIBUTE CR).
    //   2. IgaAdoptCancel.cancel — cancel every PENDING ADOPT_* CR + clear the
    //      realm's sidecar register (reused as-is; it uses the replay session's
    //      EntityManager, no inner runJobInTransaction, so it runs in THIS tx).
    //   3. cache eviction — user-cache + realm client/role/group/scope/org/idp
    //      cache, so post-disable reads reflect the IGA-off state.
    //   4. RS256 revert — on a Tide realm (tide IdP + tide-vendor-key present) whose
    //      defaultSignatureAlgorithm is EdDSA, revert to RS256 (no Tide signing path
    //      once IGA is off).
    //
    // Re-ON-safe / revokes NOTHING: this teardown does NOT burn any pack, flip any
    // MODE, or retire any VVK/VRK; IGA_ROLE_POLICY is left intact as audit. A later
    // OFF→ON toggle re-runs the ADOPT scan and re-enables cleanly.
    //
    // STUB / no ORK: DISABLE_IGA is NOT a producer-envelope-signed action
    // (TideAttestor.isProducerEnvelopeSignedAction returns false), so combineFinal
    // produced a stub signature — there is NO ORK/Policy:1 round-trip here and OFF is
    // never blocked by ORK reachability. The dispatcher tail then sets STATUS=APPROVED.
    // -------------------------------------------------------------------------

    private static void replayDisableIga(KeycloakSession session, RealmModel realm,
                                         List<Map<String, Object>> rows, EntityManager em) {
        if (realm == null) {
            throw new IllegalStateException("DISABLE_IGA replay: realm not loadable");
        }
        // Bind the realm onto the session context for the cancel/cache helpers
        // that expect a realm-bound session (mirrors IgaAdoptCancel.cancel).
        session.getContext().setRealm(realm);

        // 1. isIGAEnabled=false. IGA_REPLAY_ACTIVE is already true (replay() set
        //    it), so IgaRealmAdapter.setAttribute short-circuits to super — this is
        //    a real flag write, NOT a re-captured CR. The ROWS_JSON row carries
        //    NAME=isIGAEnabled VALUE=false for audit; we apply it directly here.
        realm.setAttribute("isIGAEnabled", "false");
        log.infof("IGA DISABLE_IGA replay: realm %s — isIGAEnabled set to false (committed via governed change request).",
                realm.getName());

        // 2. Cancel PENDING ADOPT CRs + clear sidecar (reused as-is; runs in this tx).
        org.tidecloak.iga.services.IgaAdoptCancel.CancelResult cancel =
                org.tidecloak.iga.services.IgaAdoptCancel.cancel(session, realm);
        log.infof("IGA DISABLE_IGA replay: realm %s — cancelled %d PENDING ADOPT CR(s), cleared %d sidecar row(s).",
                realm.getName(), cancel.cancelledAdoptCrs, cancel.sidecarRowsCleared);

        // 3. Evict the user-cache + realm client/role/group/scope/org/idp cache so
        //    post-disable reads reflect the IGA-off state (the IGA quarantine no
        //    longer applies). Same shared helpers the toggle endpoint uses.
        org.tidecloak.iga.services.IgaRealmCacheEviction.evictRealmUserCache(session, realm);
        org.tidecloak.iga.services.IgaRealmCacheEviction.evictRealmCache(session, realm);

        // 4. RS256 revert on a Tide realm whose default sig algorithm is EdDSA.
        //    EdDSA on a realm is only valid while a Tide signing path exists
        //    (tide IdP + tide-vendor-key) AND IGA is on; with IGA now off, revert
        //    to RS256. IGA_REPLAY_ACTIVE keeps this from being captured. Mirrors
        //    the inline OFF-toggle logic this teardown replaces.
        org.keycloak.models.IdentityProviderModel tideIdp =
                session.identityProviders().getByAlias("tide");
        org.keycloak.component.ComponentModel tideVendorKey = realm.getComponentsStream()
                .filter(x -> "tide-vendor-key".equals(x.getProviderId()))
                .findFirst()
                .orElse(null);
        if (tideIdp != null && tideVendorKey != null) {
            String currentAlgorithm = realm.getDefaultSignatureAlgorithm();
            if ("EdDSA".equalsIgnoreCase(currentAlgorithm)) {
                realm.setDefaultSignatureAlgorithm("RS256");
                log.infof("IGA DISABLE_IGA replay: realm %s — default signature algorithm reverted to RS256.",
                        realm.getName());
            }
        }
    }

    // -------------------------------------------------------------------------
    // Governed Ragnarok realm offboard (OFFBOARD_REALM)
    //
    // Captured as an irreversible, admin-approved (min-3) CR (mirroring
    // DISABLE_IGA). The REAL teardown lives in ragnarok; iga-core does NOT depend
    // on ragnarok, so the teardown is reached via the RagnarokOffboardService SPI
    // looked up by TYPE on the session. This runs INSIDE the replay tx
    // (IGA_REPLAY_ACTIVE is already true) so any model writes the offboard performs
    // pass straight through the IGA capture wrappers, and an exception rolls the
    // whole replay tx back — nothing is torn down and the CR stays committable-retry.
    //
    // FAIL-CLOSED when the SPI is absent: if ragnarok is not deployed,
    // session.getProvider(RagnarokOffboardService.class) returns null. We MUST NOT
    // silently flip STATUS=APPROVED (that would "commit" an offboard that never ran)
    // — instead we throw, rolling back the replay tx so the CR remains committable
    // once ragnarok ships.
    //
    // STUB CR ATTESTATION + REAL DOKEN CARRIER (two separate things):
    //  * OFFBOARD_REALM is NOT a producer-envelope-signed action
    //    (TideAttestor.isProducerEnvelopeSignedAction returns false), so combineFinal
    //    produced a STUB signature for the CR's own ATTESTATION column — no ORK/Policy:1
    //    round-trip gates the CR attestation.
    //  * BUT the destructive Midgard.Offboard ORK ceremony the SPI runs needs a quorum of
    //    admin DOKENS. Those are collected on the CR's REQUEST_MODEL via a real Offboard:1
    //    carrier that ragnarok seeds (svc.buildOffboardApprovalCarrier, on the first open)
    //    and the two-phase enclave approval accumulates into. We hand that accumulated
    //    carrier to svc.offboardRealm(...,carrierB64) here so the ceremony has its dokens.
    // The dispatcher tail then sets STATUS=APPROVED.
    // -------------------------------------------------------------------------

    private static void replayOffboardRealm(KeycloakSession session, RealmModel realm,
                                            IgaChangeRequestEntity cr, EntityManager em) {
        if (realm == null) {
            throw new IllegalStateException("OFFBOARD_REALM replay: realm not loadable");
        }
        // Bind the realm on the session context so the ragnarok teardown can rely on
        // a realm-bound session (same contract as replayDisableIga).
        session.getContext().setRealm(realm);

        org.tidecloak.iga.providers.RagnarokOffboardService svc =
                session.getProvider(org.tidecloak.iga.providers.RagnarokOffboardService.class);
        if (svc == null) {
            // Fail-closed: ragnarok not deployed → do NOT pretend the offboard ran.
            throw new IllegalStateException(
                    "OFFBOARD_REALM replay: no RagnarokOffboardService provider registered (ragnarok not "
                            + "deployed). Refusing to commit an offboard that did not run — the replay tx rolls "
                            + "back and CR " + realm.getName() + " stays committable-retry until ragnarok ships.");
        }

        // The accumulated Offboard:1 doken carrier the two-phase approval built up on the CR's
        // REQUEST_MODEL (seeded by svc.buildOffboardApprovalCarrier on the first open, then each
        // approving admin's enclave appended a doken). The destructive Midgard.Offboard ORK
        // ceremony needs these dokens — fail-closed if the carrier is missing (an offboard can
        // never run without its admin-quorum dokens).
        String dokenCarrierB64 = cr == null ? null : cr.getRequestModel();
        if (dokenCarrierB64 == null || dokenCarrierB64.isBlank()) {
            throw new IllegalStateException(
                    "OFFBOARD_REALM replay: CR has no accumulated Offboard:1 doken carrier "
                            + "(REQUEST_MODEL is null/blank) for realm " + realm.getName()
                            + " — refusing to run Midgard.Offboard without admin-quorum dokens. The replay tx "
                            + "rolls back and the CR stays committable-retry.");
        }

        try {
            org.tidecloak.iga.providers.RagnarokOffboardResult result =
                    svc.offboardRealm(session, realm, em, dokenCarrierB64);
            log.infof("IGA OFFBOARD_REALM replay: realm %s — ragnarok offboard complete (success=%s): %s",
                    realm.getName(),
                    result != null && result.success(),
                    result != null ? result.summary() : "(no summary)");
        } catch (org.tidecloak.iga.providers.RagnarokOffboardException e) {
            // Surface as an unchecked exception so the replay tx rolls back (nothing
            // torn down) and the commit fails with a clear message.
            throw new RuntimeException(
                    "OFFBOARD_REALM replay: ragnarok offboard failed for realm " + realm.getName()
                            + " — replay tx rolled back, nothing torn down: " + e.getMessage(), e);
        }
    }

