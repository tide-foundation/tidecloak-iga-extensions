    private static void replayRelationship(KeycloakSession session, RealmModel realm,
                                            List<Map<String, Object>> rows, String sig, EntityManager em,
                                            String updateJpql, String rowKey1, String rowKey2,
                                            Consumer<Map<String, Object>> addOp,
                                            boolean setSigned, String actionType) {
        for (Map<String, Object> row : rows) {
            addOp.accept(row);
        }
        if (sig == null || sig.isEmpty()) return;

        // SET-SIGNING fan-out (tide attestor only): stamp the ONE set signature
        // onto the WHOLE owner set (every row sharing the owner key), NOT just
        // the changed row. Owner mapping comes from the SAME TideSetResolver the
        // attestor used to compute the set signature.
        if (setSigned && stampOwnerSetFanOut(em, actionType, rows, sig)) {
            return;
        }

        // PER-ROW stamp (simple attestor / non-linkage) — UNCHANGED behaviour.
        for (Map<String, Object> row : rows) {
            String k1 = str(row, rowKey1);
            String k2 = str(row, rowKey2);
            if (k1 == null || k2 == null) continue;
            em.createQuery(updateJpql)
                    .setParameter("sig", sig)
                    .setParameter("k1", k1)
                    .setParameter("k2", k2)
                    .executeUpdate();
        }
    }

    private static void replayRevoke(KeycloakSession session, RealmModel realm,
                                      List<Map<String, Object>> rows, String sig, EntityManager em,
                                      Consumer<Map<String, Object>> removeOp,
                                      boolean setSigned, String actionType) {
        for (Map<String, Object> row : rows) {
            removeOp.accept(row);
        }
        // SET-SIGNING re-sign-on-remove (tide attestor only): the removed row is
        // gone, but the SURVIVING rows of the owner set must be re-stamped with
        // the new (shrunken-set) signature. The simple attestor never stamps on
        // a revoke — that per-row no-stamp behaviour is preserved when
        // !setSigned (sig is ignored, exactly as the old 4-arg replayRevoke).
        if (setSigned && sig != null && !sig.isEmpty()) {
            stampOwnerSetFanOut(em, actionType, rows, sig);
        }
    }

    /**
     * Fan the set signature out across the WHOLE owner set for a LINKAGE action,
     * keyed by the owner column only (no member predicate). Returns {@code true}
     * iff the action maps to a linkage table (and the fan-out ran); {@code false}
     * for non-linkage actions so the caller falls back to its per-row stamp.
     *
     * <p>protocol_mapper owners can be a client OR a client_scope; the owner
     * value+field are resolved per row (CLIENT_UUID/CLIENT_ID → client.id, else
     * CLIENT_SCOPE_ID → clientScope.id). All other linkages use the descriptor's
     * single owner field.
     */
    private static boolean stampOwnerSetFanOut(EntityManager em, String actionType,
                                               List<Map<String, Object>> rows, String sig) {
        org.tidecloak.iga.attestors.TideSetResolver.Linkage linkage =
                org.tidecloak.iga.attestors.TideSetResolver.linkageFor(actionType);
        if (linkage == null) return false;

        // De-dup owners across the CR's rows so we issue one UPDATE per owner.
        java.util.LinkedHashMap<String, String> ownerToField = new java.util.LinkedHashMap<>();
        for (Map<String, Object> row : rows) {
            String owner;
            String ownerField;
            if ("protocol_mapper".equals(linkage.table())) {
                String clientUuid = str(row, "CLIENT_UUID");
                String clientId = str(row, "CLIENT_ID");
                if (clientUuid != null) {
                    owner = clientUuid;
                    ownerField = linkage.ownerField(); // client.id
                } else if (clientId != null) {
                    owner = clientId;
                    ownerField = linkage.ownerField();
                } else {
                    owner = str(row, "CLIENT_SCOPE_ID");
                    ownerField = org.tidecloak.iga.attestors.TideSetResolver.PROTOCOL_MAPPER_SCOPE_OWNER_FIELD;
                }
            } else {
                owner = str(row, linkage.ownerRowKey());
                ownerField = linkage.ownerField();
            }
            if (owner != null) ownerToField.putIfAbsent(owner, ownerField);
        }
        for (Map.Entry<String, String> e : ownerToField.entrySet()) {
            em.createQuery("UPDATE " + linkage.entityName() + " e SET e.attestation = :sig WHERE e."
                            + e.getValue() + " = :owner")
                    .setParameter("sig", sig)
                    .setParameter("owner", e.getKey())
                    .executeUpdate();
        }
        return true;
    }

    /**
     * SET-SIGNING for the NESTED CHILDREN of a node-create (tide attestor only).
     *
     * <p>A node-create CR ({@code CREATE_ROLE}/{@code CREATE_CLIENT_SCOPE}/
     * {@code CREATE_CLIENT}) carries the node's own per-entity signature. When it
     * also created child/linkage rows inline (composites of the role; protocol
     * mappers of the scope/client), those child rows must — under set-signing —
     * carry their OWN per-(table, owner) SET signature with {@code owner = the
     * just-created node}, NOT the node's per-entity sig. This makes the child set
     * independently re-derivable exactly like the dedicated linkage actions
     * ({@code ADD_COMPOSITE}, {@code ADD_PROTOCOL_MAPPER}, ...) produce.
     *
     * <p>Post-persist the children already exist, so we read the owner's member
     * set straight off the DB (no PRE-change+delta reconstruction), compute the
     * set sig via {@link org.tidecloak.iga.attestors.TideAttestor#signSet}, and
     * fan it out across the WHOLE owner set via the linkage's owner-keyed UPDATE
     * (the SAME {@link org.tidecloak.iga.attestors.TideSetResolver.Linkage}
     * fan-out the dedicated linkage actions use). No-op (no UPDATE, no empty-set
     * sig) when the node created no such children — e.g. a {@code CREATE_ROLE}
     * with no composites.
     *
     * @param actionType the LINKAGE action whose descriptor maps the child set
     *                   (e.g. {@code ADD_COMPOSITE} for composite_role,
     *                   {@code ADD_PROTOCOL_MAPPER} for protocol_mapper).
     * @param ownerId    the just-created node's id (composite=parent role id;
     *                   protocol_mapper owner = scope id OR client id).
     * @param ownerField the JPA owner field to group/fan-out by (composite_role:
     *                   {@code parentRole.id}; protocol_mapper scope-owned:
     *                   {@code clientScope.id}; protocol_mapper client-owned:
     *                   {@code client.id}).
     */
    private static void signNestedChildSet(KeycloakSession session, EntityManager em,
                                           String actionType, String ownerId, String ownerField) {
        if (ownerId == null) return;
        org.tidecloak.iga.attestors.TideSetResolver.Linkage linkage =
                org.tidecloak.iga.attestors.TideSetResolver.linkageFor(actionType);
        if (linkage == null) return;

        // Read the owner's POST-change member set straight off the DB (children
        // already persisted by the node-create's RepresentationToModel rebuild).
        @SuppressWarnings("unchecked")
        List<Object> members = em.createQuery(
                        "SELECT e." + linkage.memberField() + " FROM " + linkage.entityName()
                                + " e WHERE e." + ownerField + " = :owner")
                .setParameter("owner", ownerId)
                .getResultList();
        if (members == null || members.isEmpty()) return; // no children → no set, no-op

        java.util.List<String> memberIds = new java.util.ArrayList<>();
        for (Object m : members) {
            if (m != null) memberIds.add(m.toString());
        }
        if (memberIds.isEmpty()) return;

        // Compute the set sig over the EXACT (table, owner, members) canonical the
        // dedicated linkage actions sign — single crypto swap-point in TideAttestor.
        org.tidecloak.iga.attestors.TideAttestor attestor =
                new org.tidecloak.iga.attestors.TideAttestor(session);
        String setSig = attestor.signSet(session, linkage.table(), ownerId, memberIds);

        // Fan the set sig out across the WHOLE owner set (owner-keyed only).
        em.createQuery("UPDATE " + linkage.entityName() + " e SET e.attestation = :sig WHERE e."
                        + ownerField + " = :owner")
                .setParameter("sig", setSig)
                .setParameter("owner", ownerId)
                .executeUpdate();
    }

